package com.prowings.polymorphism.overloading;

public class Method {
	
	public void addition(int a) {
		
		System.out.println("addition(int a) method");
		
	}
	public void addition(int a,long b) {
		
		System.out.println("addition(int a,long b) method");
		
	}
	public void addition(String s,int a) {
		
		System.out.println("addition(string s,int a) method");
		
	}

	
}

